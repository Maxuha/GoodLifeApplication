package life.good.goodlife.model.declension;

public class Declension {
    private String Р;
    private String Д;
    private String В;
    private String Т;
    private String П;

    public String getР() {
        return Р;
    }

    public void setР(String р) {
        Р = р;
    }

    public String getД() {
        return Д;
    }

    public void setД(String д) {
        Д = д;
    }

    public String getВ() {
        return В;
    }

    public void setВ(String в) {
        В = в;
    }

    public String getТ() {
        return Т;
    }

    public void setТ(String т) {
        Т = т;
    }

    public String getП() {
        return П;
    }

    public void setП(String п) {
        П = п;
    }
}
